class ExactTreeEasy {
public:
	vector <int> getTree(int, int);
};

vector <int> ExactTreeEasy::getTree(int n, int m) {
	vector<int> vec;
	if(m==n-1){
		for(int i = 1 ; i<n ; i++){
			vec.push_back(0);
			vec.push_back(i);
		}
		return vec;
	}
	int M=m;
	vec.push_back(0);
	vec.push_back(1);
	vec.push_back(0);
	vec.push_back(2);
	if(m==1 && n==3)
	return vec;
	m--;
	int current = 2;
	while(current+m<n-1){
		vec.push_back(current);
		vec.push_back(current+1);
		current++;
	}
	for(int i=current+1; i<n ; i++){
		vec.push_back(current);
		vec.push_back(i);
	}
	return vec;
	
}


