class PotentialGeometricSequence {
public:
	int numberOfSubsequences(vector <int> d) {
		if(d.size()==0)
		return 1;
		int counter=d.size();
		for(int i = 1 ; i <d.size() ;i++){
			for(int j = 0 ; j<=d.size()-i-1 ; j++){
				int diff = d[j]-d[j+1];
				int k;
				for(k = j+1 ; k<j+i ;k++)
				if(d[k]-d[k+1]!=diff)
				break;
				if(k==j+i)
				counter++;
			}
		}
		return counter;
	}
};


