class RepeatStringEasy {
public:
	string str1,str2;
	int maximalLength(string);
	int dp[50][50];
	int calc(int i,int j);
};

int RepeatStringEasy::maximalLength(string s) {
	int max=0;
	for(int i = 0 ; i < s.size()-1 ; i++){
		str1 = s.substr(0,i+1);
		str2 = s.substr(i+1,s.size()-i-1);
		memset(dp,-1,sizeof(dp));
		int local = calc(i,s.size()-i-2);
		if(local > max)
		max=local;
	}
	return (max*2);
}

int RepeatStringEasy::calc(int i,int j){
	if(i<0 || j<0)
	return 0;
	int local=0;
	if(dp[i][j]!=-1)
	return dp[i][j];
	else if(str1[i]==str2[j]){
		local = 1+calc(i-1,j-1);
	}
	else if(str1[i]!=str2[j]){
		local = max(calc(i-1,j),calc(i,j-1));
	}
	dp[i][j]=local;
	return local;
}


