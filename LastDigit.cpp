class LastDigit {
public:
	long long findX(long long);
	long long recur(long long x);
};

long long LastDigit::findX(long long S) {
	long long int low = 1, high=1000000000000000000;
	while(low<=high){
		long long mid = (high-low)/2 + low;
		if(recur(mid)<S)
		low = mid+1;
		else if(recur(mid) > S)
		high = mid - 1;
		else
		return mid;
	}
	return -1;
}

long long LastDigit::recur(long long x){
	long long sum=0;
	while(x>0){
		sum+=x;
		x/=10;
	}
	return sum;
}


