class BearDartsDiv2 {
public:
	long long count(vector <int>);

};

long long BearDartsDiv2::count(vector <int> w) {
	int n = w.size();
	
	vector<int> present(1000001,-1);
	for(int i = 0 ; i<n ; i++){
		present[w[i]]=i;
	}
	
	vector< vector<int> > time_present(n, vector<int>(n+1,0));
	
	for(int i = 0 ; i< n ;i++){
		for(int j =0 ; j<=n ; j++){
			for(int k = j ; k< n ;k++){
				if(w[i]==w[k])
				time_present[i][j]++;
			}
		}
	}
	
	
	long long  res= 0 ;
	
	for(int i = 0 ; i<n ; i++){
		for(int j = i+1 ; j< n ; j++){
			for(int k = j+1 ; k<n ; k++){
				
				long long int ans = w[i];
				ans = ans * w[j];
				if(ans>1000000)
				continue;
				ans = ans *w[k];				
				if(ans<=1000000 && present[ans]!=-1){
					res+=time_present[present[ans]][k+1];
				}
			}
		}
	}
	return res;
}


